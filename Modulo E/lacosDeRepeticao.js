//Laço de repetição com while
var cont = 1
while(cont <= 15){
    console.log(`Repete ${cont} vezes`)
    cont++
}

//__________________________________________

//Laço de repetição com do while
var cont2 = 1
do{
    console.log(`Repetiu - ${cont2}`)
    cont2++
}while(cont2 <= 5)

//___________________________________________

//Laço de repetição com for
for(var cont3 = 1; cont3 <= 10; cont3++){
    console.log(cont3)
}
console.log('FIM!')